function colorit() {
	var mypoly;
	if (document.all) {
		index = selectVarForm.myVar.selectedIndex;
	}
	else {
		index = document.selectvariable.document.selectVarForm.myVar.selectedIndex;
	}
	var myArray = new Array();
	var myThresholds = new Array();
	if (document.all) {
		svgdoc = svgFile1.getSVGDocument();
	}
	else {
		svgdoc = document.svgplugin.document.embeds[0].getSVGDocument()
	}
	
	window.status = "determining quantile values ...";
	
	//sort for quantile calculation
	if (document.all) {
		nrClasses = selectClassesForm.myClass.value
	}
	else {
		nrClasses = document.selectnrclasses.document.selectClassesForm.myClass.options[document.selectnrclasses.document.selectClassesForm.myClass.options.selectedIndex].value			
	}
	for (i = 0; i < ids.length; i++) {
		mypoly = "poly"+ids[i];
		myArray[i] = eval(mypoly)[index];		
	}
	myArray.sort(compareNumbers);
	
	//get quantil values
	threshold = myArray.length / nrClasses;
	myThreshold = threshold;
	myThresholds[0] = myArray[0] - 0.01;
	for (i = 1; i < nrClasses; i++) {
		myThresholds[i] = myArray[Math.round(myThreshold)];
		myThreshold = myThreshold + threshold;
	}
	myThresholds[i] = myArray[myArray.length - 1];
	
	//set array with color-values
	window.status = "setting color values ...";
	if (nrClasses == 2) {
		var myColors = new Array("#FFFDDA","#FF6C11");
	}
	if (nrClasses == 3) {
		var myColors = new Array("#FFFDDA","#FFB576","#FF6C11");
	}
	if (nrClasses == 4) {
		var myColors = new Array("#FFFDDA","#FFCD96","#FF9C54","#FF6C11");
	}
	if (nrClasses == 5) {
		var myColors = new Array("#FFFDDA","#FFDAA7","#FFB474","#FF9143","#FF6C11"); //from light yellow to red
	}
		
	//color it
	window.status = "colouring map ...";
	for (i = 0; i < ids.length; i++) {
		myid = "bebaut"+ids[i];
		mypoly = "poly"+ids[i];
		myValue = eval(mypoly)[index];
		svgObj = svgdoc.getElementById(myid);
		svgStyle = svgObj.getStyle();
		for (j = 0; j < myColors.length; j++) {
			if ((myValue > myThresholds[j]) && (myValue <= myThresholds[j+1])) {
				svgStyle.setProperty('fill',myColors[j]);
			}
		}
	}

	//set legend
	window.status = "setting legend ...";
	if (document.all) {
		legenddoc = svgFile2.getSVGDocument();
	}
	else {
		legenddoc = document.svglegend.document.embeds[0].getSVGDocument();
	}		

	legendObj = legenddoc.getElementById("headline");
        legendObj = legendObj.getFirstChild();
	if (document.all) {
        	legendObj.setData(selectVarForm.myVar.options[selectVarForm.myVar.selectedIndex].text);
        }
        else {
        	legendObj.setData(document.selectvariable.document.selectVarForm.myVar.options[document.selectvariable.document.selectVarForm.myVar.selectedIndex].text);
        }

	for (i = 0; i < 5; i++) {
		legendObj = legenddoc.getElementById("lval"+i);
		legendStyle = legendObj.getStyle();
		legendStyle.setProperty('visibility','hidden');		
	}

	for (i = 0; i < nrClasses; i++) {
		legendObj = legenddoc.getElementById("textVal"+i);
                legendObj = legendObj.getFirstChild();
                legendObj.setData("< "+myThresholds[i+1]);		
		legendObj = legenddoc.getElementById("val"+i);
		legendStyle = legendObj.getStyle();
		legendStyle.setProperty('fill',myColors[i]);
		legendObj = legenddoc.getElementById("lval"+i);
		legendStyle = legendObj.getStyle();
		legendStyle.setProperty('visibility','visible');
	}

	//get a references to svgvalues.svg and its objects
	if (document.all) {
		svgvaldoc = svgVal.getSVGDocument()
	}
	else {
		svgvaldoc = document.svgvalues.document.embeds[0].getSVGDocument()
	}				
	svgdistrictObj = svgvaldoc.getElementById("textDistrict")
	svgdistrictObj = svgdistrictObj.getFirstChild()
	svgsubdistrictObj = svgvaldoc.getElementById("textSubdistrict")
	svgsubdistrictObj = svgsubdistrictObj.getFirstChild()
	svgvalObj = svgvaldoc.getElementById("textValue")
	svgvalObj = svgvalObj.getFirstChild()
	
	window.status = "done ...";

}

